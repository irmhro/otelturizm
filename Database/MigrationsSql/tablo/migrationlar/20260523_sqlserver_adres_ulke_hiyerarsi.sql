IF COL_LENGTH(N'dbo.ILLER', N'DIS_KOD') IS NULL
BEGIN
    ALTER TABLE [dbo].[ILLER] ADD [DIS_KOD] nvarchar(16) NULL;
END
GO

DECLARE @trUlkeId bigint = (
    SELECT TOP (1) [ID]
    FROM [dbo].[ULKELER]
    WHERE [AKTIF_MI] = 1 AND RTRIM([ISO2_KODU]) = N'TR'
    ORDER BY [VARSAYILAN_ULKE] DESC, [ID] ASC
);

IF @trUlkeId IS NOT NULL
BEGIN
    UPDATE [dbo].[ILLER]
    SET [ULKE_ID] = @trUlkeId,
        [BOLGE_TIPI] = COALESCE(NULLIF(LTRIM(RTRIM([BOLGE_TIPI])), N''), N'IL'),
        [DIS_KOD] = COALESCE(NULLIF(LTRIM(RTRIM([DIS_KOD])), N''), RIGHT(N'00' + CAST([PLAKA_KODU] AS nvarchar(3)), 2))
    WHERE [ULKE_ID] IS NULL;
END
GO

UPDATE [dbo].[ILLER]
SET [BOLGE_TIPI] = N'IL'
WHERE [BOLGE_TIPI] IS NULL OR LTRIM(RTRIM([BOLGE_TIPI])) = N'';
GO

IF EXISTS (
    SELECT 1 FROM [dbo].[ILLER]
    WHERE [ULKE_ID] IS NULL
)
BEGIN
    RAISERROR(N'ILLER.ULKE_ID backfill tamamlanamadi. Once ULKELER (TR) seed uygulanin.', 16, 1);
END
GO

