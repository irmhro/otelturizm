DECLARE @trUlkeId2 bigint = (
    SELECT TOP (1) [ID]
    FROM [dbo].[ULKELER]
    WHERE [AKTIF_MI] = 1 AND RTRIM([ISO2_KODU]) = N'TR'
    ORDER BY [VARSAYILAN_ULKE] DESC, [ID] ASC
);

IF @trUlkeId2 IS NOT NULL
BEGIN
    UPDATE c
    SET c.[ULKE_ID] = @trUlkeId2
    FROM [dbo].[ILCELER] AS c
    WHERE c.[ULKE_ID] IS NULL;

    UPDATE m
    SET m.[ULKE_ID] = @trUlkeId2
    FROM [dbo].[MAHALLELER] AS m
    WHERE m.[ULKE_ID] IS NULL;
END
GO

